    private void buscarAlunoPorId(int id) {
        try {
            tableModel.limpaTabela();
            ArrayList<Aluno> dados = ControladorPrincipal.getInstance().getAlunoPorId(id);
            for (int i = 0; i < dados.size(); i++) {
                Aluno a = dados.get(i);
                a.getId();
                a.getNome();
                a.getCpf();
                a.getRg();
                a.getNascimento();
                a.getTelefone();
                a.getEndereco();
                tableModel.addRow(a);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

