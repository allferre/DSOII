    private void cadastrarAluno() {
        try {
            String nomeFunc = jTextField1.getText();
            String cpfFunc = jTextField2.getText();
            String rgFunc = jTextField3.getText();
            String nascimentoFunc = jTextField4.getText();
            String telefoneFunc = jTextField5.getText();
            String enderecoFunc = jTextField6.getText();
            int id = Integer.parseInt(jTextField27.getText());

            Aluno a = new Aluno(id, nomeFunc, cpfFunc, rgFunc, nascimentoFunc, telefoneFunc, enderecoFunc);
            ControladorPrincipal.getInstance().insereAluno(a);

            JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            setarIdAluno();

        } catch (Exception e) {
            System.out.println(e);
        }

    }

    private void setarIdAluno() throws SQLException {
        jTextField27.setText(String.valueOf(ControladorPrincipal.getInstance().getUltimoIdAluno() + 1));
    }
